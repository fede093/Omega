CREATE PROCEDURE spInsertarUsuario
(
@USUARIO NVARCHAR(50),
@NOMBRE NVARCHAR(50),
@APELLIDO NVARCHAR(50),
@EMAIL NVARCHAR(50),
@CONTRASEÑA NVARCHAR(50),
@TELEFONO NVARCHAR (50),
@COD_PAIS INT,
@ADMINISTRADOR BIT
)
AS
INSERT INTO Usuario
(
Usuario,
Nombre,
Apellido,
Email,
Contraseña,
Telefono,
Cod_Pais,
Administrador
)
VALUES
(
@USUARIO,
@NOMBRE,
@APELLIDO,
@EMAIL,
@CONTRASEÑA,
@TELEFONO,
@COD_PAIS,
@ADMINISTRADOR
)RETURN
GO